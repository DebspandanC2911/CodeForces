bool prime(int x){
    if (x <= 1) {
        return false;
    }
    for (int i = 2; i * i <= x; i++) {
        if (x % i == 0) {
            return false;
        }
    }
    return true;
}
void solve(){
    ll n,k; 
    cin>>n>>k; 
    if(n>1 && k>1)  cout<<"NO"<<endl;
    else if(k==1)   cout<<((prime(n))?"YES":"NO")<<endl;
    else cout<<((k==2)?"YES":"NO")<<endl;    
}
